const sayings = [];
const proverbs = [];

function showRandom(type) {
  let list;
  switch (type) {
    case 'word':
      list = words;
      break;
    case 'saying':
      list = sayings;
      break;
    case 'proverb':
      list = proverbs;
      break;
  }
  const index = Math.floor(Math.random() * list.length);
  document.getElementById(type).textContent = list[index];
}

